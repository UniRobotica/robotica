function Ex1_2_1_CinPerna

clc
close all

% =========================================================
% Figura
% =========================================================

fig = figure( ...
    'Name','Segmento Interativo', ...
    'Color','w', ...
    'Position',[100 100 700 550]);

% =========================================================
% Eixos
% =========================================================

ax = axes( ...
    'Parent',fig, ...
    'Position',[0.10 0.35 0.85 0.60]);

grid(ax,'on')

axis(ax,'equal')
axis(ax,[-12 12 -12 12])

xlabel(ax,'x')
ylabel(ax,'y')

title(ax,'Segmento Rotacionando')

hold(ax,'on')

% =========================================================
% Slider do angulo
% =========================================================

uicontrol( ...
    'Style','text', ...
    'Position',[60 120 140 20], ...
    'String','Angulo [graus]');

s1 = uicontrol( ...
    'Style','slider', ...
    'Min',-180, ...
    'Max',180, ...
    'Value',45, ...
    'Position',[60 100 250 20], ...
    'Callback',@updatePlot);

% =========================================================
% Slider do comprimento
% =========================================================

uicontrol( ...
    'Style','text', ...
    'Position',[380 120 140 20], ...
    'String','Comprimento L');

s2 = uicontrol( ...
    'Style','slider', ...
    'Min',1, ...
    'Max',10, ...
    'Value',5, ...
    'Position',[380 100 250 20], ...
    'Callback',@updatePlot);

% =========================================================
% Primeira atualizacao
% =========================================================

updatePlot()

% =========================================================
% Funcao interna
% =========================================================

    function updatePlot(~,~)

        % ================================================
        % Valores dos sliders
        % ================================================

        thetaDeg = get(s1,'Value');
        L = get(s2,'Value');

        % Conversao para radianos
        theta = deg2rad(thetaDeg);

        % ================================================
        % Coordenadas
        % ================================================

        x = L*cos(theta);
        y = L*sin(theta);

        % ================================================
        % Limpa eixo
        % ================================================

        cla(ax)

        grid(ax,'on')

        axis(ax,'equal')
        axis(ax,[-12 12 -12 12])

        hold(ax,'on')

        % ================================================
        % Segmento
        % ================================================

        plot(ax,[0 x],[0 y], ...
            'LineWidth',4, ...
            'Color',[0.10 0.40 0.80])

        % ================================================
        % Extremidade
        % ================================================

        plot(ax,x,y,'ro', ...
            'MarkerSize',12, ...
            'MarkerFaceColor','r')

        % ================================================
        % Origem
        % ================================================

        plot(ax,0,0,'ko', ...
            'MarkerSize',8, ...
            'MarkerFaceColor','k')

        % ================================================
        % Informacoes no titulo
        % ================================================

        title(ax, ...
            sprintf([ ...
            'Angulo = %.1f°   |   L = %.2f\n' ...
            'x = %.2f   |   y = %.2f'], ...
            thetaDeg,L,x,y));

    end

end